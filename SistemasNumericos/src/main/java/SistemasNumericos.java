
public class SistemasNumericos {

    public static void main(String[] args) {
        int numeroDecimal = 10;
        System.out.println("numeroDecimal = " + numeroDecimal);

        int numeroOctal = 012;
        /* Para definir que es Octal se pone un "cero" 
           delante del número. */
        System.out.println("numeroOctal = " + numeroOctal);

        int numeroHexadecimal = 0xA;
        /* Para definir que es un sistema Hexadecimal 
        ponemos delante un "cero y una X( que puede ser mayuscula o minuscula)".
         */
        System.out.println("numeroHexadecimal = " + numeroHexadecimal);

        int numeroBinario = 0b1010;
        /* Para definir un binario ponemos
        "cero y B". */
        System.out.println("numeroBinario = " + numeroBinario);
        
     // Podemos utilizar la palabra reservada "var" para declarar éstos ejemplos

    }

}
